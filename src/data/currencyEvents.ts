// 获取影响指定货币对的事件
export function getEventsForCurrencyPair(
  from: CurrencyCode,
  to: CurrencyCode,
  startDate?: string,
  endDate?: string
): CurrencyEvent[] {
  return CURRENCY_EVENTS.filter((event) => {
    // 检查事件是否影响这对货币
    const affectsFrom = event.currencies.includes(from);
    const affectsTo = event.currencies.includes(to);
    
    if (!affectsFrom && !affectsTo) return false;
    
    // 检查日期范围
    if (startDate && event.date < startDate) return false;
    if (endDate && event.date > endDate) return false;
    
    return true;
  }).sort((a, b) => a.date.localeCompare(b.date));
}

// 获取事件的影响方向（对于指定货币对）
export function getEventImpactDirection(
  event: CurrencyEvent,
  from: CurrencyCode,
  to: CurrencyCode
): 'up' | 'down' | 'neutral' {
  const affectsFrom = event.currencies.includes(from);
  const affectsTo = event.currencies.includes(to);
  
  if (event.impact === 'neutral') return 'neutral';
  
  // 如果事件只影响基准货币
  if (affectsFrom && !affectsTo) {
    return event.impact === 'positive' ? 'up' : 'down';
  }
  
  // 如果事件只影响目标货币
  if (!affectsFrom && affectsTo) {
    return event.impact === 'positive' ? 'down' : 'up';
  }
  
  // 如果两种货币都受影响，返回中性
  return 'neutral';
}
